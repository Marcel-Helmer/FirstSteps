package Uebungen;

public class Wochentage {
    public static void main(String[] args) {

        String[] wochentag;
        wochentag = new String[7];
        wochentag[0] = "Montag";
        wochentag[1] = "Dienstag";
        wochentag[2] = "Mittwoch";
        wochentag[3] = "Donnerstag";
        wochentag[4] = "Freitag";
        wochentag[5] = "Samstag";
        wochentag[6] = "Sonntag";

        for (int i = 0; i < wochentag.length; i++) {

            System.out.println("Heute ist: " + wochentag[i]);
        }







    }













}
